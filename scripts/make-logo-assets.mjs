// Regenerate the shared web/PWA/logo source assets from the master brand image.
//
// Source of truth:
//   resources/brand/ultreia-original.png
//
// Outputs:
//   resources/ultreia-logo.png   — 384px transparent-corner app display logo
//   resources/ultreia-logo.webp  — same, lightweight runtime import
//   resources/splash-logo.png    — 512px transparent-corner native splash logo
//   resources/ultreia-boot-lines.png — exact frame / contour pixels for boot reveal
//   resources/ultreia-boot-mountains.png — exact mountain pixels for boot reveal
//   public/favicon.jpg           — 512px full-square PWA / favicon / launcher base
//
// Android rasters and poster marks are generated by separate scripts because
// they have their own target dimensions and theme extraction rules.
import sharp from 'sharp';
import { mkdir } from 'node:fs/promises';

const SRC = 'resources/brand/ultreia-original.png';
const ROUNDED_RADIUS_RATIO = 0.22;
const FAVICON_BG = '#1a1a1a';
const CROP_LUMA_THRESHOLD = 10;
const BOOT_LAYER_MOUNTAIN_PATHS = [
  'M57.7 370 L97.9 318.3 L146.9 262.3 L197.5 201.3 L224.7 182.8 L223.1 227.4 L189.3 261.2 L164.3 298.2 L117.5 316.7 L83.2 349.3 Z',
  'M223.1 201.3 L279.1 128.4 L314.5 101.2 L314.5 161.1 L269.9 220.9 L248.1 217.6 Z',
  'M304.7 144.7 L439.6 288.4 L331.9 251.4 L311.8 187.2 Z',
  'M279.1 166.5 L310.1 185 L305.8 206.8 L280.8 226.3 L265.5 216.6 L282.9 193.2 Z',
  'M99.6 512 L130.6 487 L167.6 438 L211.1 390.7 L251.9 350.9 L277.5 318.8 L265.5 296.5 L230.2 265.5 L211.1 243.8 L222 223.1 L254.6 206.8 L281.8 187.2 L298.2 163.2 L317.2 174.1 L310.7 209.5 L275.3 231.8 L249.2 244.8 L277.5 262.3 L321 282.9 L352.6 311.2 L328.1 340.1 L287.3 378.2 L246.5 424.4 L205.7 473.4 L176.3 512 Z',
];

function roundedMaskSvg(size) {
  const radius = Math.round(size * ROUNDED_RADIUS_RATIO);
  return Buffer.from(
    `<svg width="${size}" height="${size}" xmlns="http://www.w3.org/2000/svg">
       <rect x="0" y="0" width="${size}" height="${size}" rx="${radius}" ry="${radius}" fill="#fff"/>
     </svg>`,
  );
}

async function findLogoCrop() {
  const { data, info } = await sharp(SRC)
    .removeAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });
  const { width, height, channels } = info;
  let minX = width;
  let minY = height;
  let maxX = -1;
  let maxY = -1;

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const i = (y * width + x) * channels;
      const r = data[i];
      const g = data[i + 1];
      const b = data[i + 2];
      const luma = 0.299 * r + 0.587 * g + 0.114 * b;
      if (luma <= CROP_LUMA_THRESHOLD) continue;
      if (x < minX) minX = x;
      if (y < minY) minY = y;
      if (x > maxX) maxX = x;
      if (y > maxY) maxY = y;
    }
  }

  if (maxX < minX || maxY < minY) {
    return { left: 0, top: 0, width, height };
  }

  const contentW = maxX - minX + 1;
  const contentH = maxY - minY + 1;
  const side = Math.min(Math.max(contentW, contentH), width, height);
  const cx = (minX + maxX) / 2;
  const cy = (minY + maxY) / 2;
  const left = Math.max(0, Math.min(width - side, Math.round(cx - side / 2)));
  const top = Math.max(0, Math.min(height - side, Math.round(cy - side / 2)));
  return { left, top, width: side, height: side };
}

const logoCrop = await findLogoCrop();

async function roundedLogo(size) {
  const base = await sharp(SRC)
    .extract(logoCrop)
    .resize(size, size, { fit: 'cover', position: 'centre' })
    .ensureAlpha()
    .png()
    .toBuffer();

  return sharp(base)
    .composite([{ input: roundedMaskSvg(size), blend: 'dest-in' }])
    .png()
    .toBuffer();
}

function mountainMaskSvg(strokeWidth = 20) {
  return Buffer.from(
    `<svg width="512" height="512" viewBox="0 0 512 512" xmlns="http://www.w3.org/2000/svg">
       <rect width="512" height="512" fill="#000"/>
       <g fill="#fff" stroke="#fff" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round">
         ${BOOT_LAYER_MOUNTAIN_PATHS.map((path) => `<path d="${path}"/>`).join('')}
       </g>
     </svg>`,
  );
}

async function makeBootLayers(sourcePng) {
  const { data, info } = await sharp(sourcePng)
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });
  const { width, height, channels } = info;
  const { data: mountainMask } = await sharp(mountainMaskSvg(20))
    .resize(width, height)
    .blur(1.3)
    .greyscale()
    .raw()
    .toBuffer({ resolveWithObject: true });
  const { data: mountainExclude } = await sharp(mountainMaskSvg(38))
    .resize(width, height)
    .blur(2)
    .greyscale()
    .raw()
    .toBuffer({ resolveWithObject: true });

  const lineLayer = Buffer.alloc(width * height * 4);
  const mountainLayer = Buffer.alloc(width * height * 4);

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const srcIndex = (y * width + x) * channels;
      const dstIndex = (y * width + x) * 4;
      const r = data[srcIndex];
      const g = data[srcIndex + 1];
      const b = data[srcIndex + 2];
      const a = data[srcIndex + 3];
      if (a === 0) continue;

      const luma = 0.299 * r + 0.587 * g + 0.114 * b;
      const greenBias = g - Math.max(r, b);
      const mountainAlpha = Math.round(a * mountainMask[y * width + x] / 255);
      const isMountainPixel = luma > 34 || (luma > 24 && greenBias > 4);
      if (isMountainPixel && mountainAlpha > 0) {
        const alpha = Math.round(mountainAlpha * Math.min(1, Math.max(0.22, (luma - 18) / 90)));
        mountainLayer[dstIndex] = r;
        mountainLayer[dstIndex + 1] = g;
        mountainLayer[dstIndex + 2] = b;
        mountainLayer[dstIndex + 3] = alpha;
      }

      const lineAlpha = Math.round(a * Math.max(0, 255 - mountainExclude[y * width + x]) / 255);
      const isLinePixel = luma > 42 || (luma > 28 && greenBias > 3);
      if (isLinePixel && lineAlpha > 0) {
        const alpha = Math.round(lineAlpha * Math.min(1, Math.max(0.18, (luma - 24) / 88)));
        if (alpha > 5) {
          lineLayer[dstIndex] = r;
          lineLayer[dstIndex + 1] = g;
          lineLayer[dstIndex + 2] = b;
          lineLayer[dstIndex + 3] = alpha;
        }
      }
    }
  }

  await sharp(lineLayer, { raw: { width, height, channels: 4 } })
    .png({ compressionLevel: 9, adaptiveFiltering: true })
    .toFile('resources/ultreia-boot-lines.png');
  await sharp(mountainLayer, { raw: { width, height, channels: 4 } })
    .png({ compressionLevel: 9, adaptiveFiltering: true })
    .toFile('resources/ultreia-boot-mountains.png');
}

await mkdir('resources', { recursive: true });
await mkdir('public', { recursive: true });

const displayPng = await roundedLogo(384);
await sharp(displayPng).toFile('resources/ultreia-logo.png');
await sharp(displayPng)
  .webp({ quality: 90, alphaQuality: 100 })
  .toFile('resources/ultreia-logo.webp');

const splashPng = await roundedLogo(512);
await sharp(splashPng).toFile('resources/splash-logo.png');
await makeBootLayers(splashPng);

await sharp(splashPng)
  .flatten({ background: FAVICON_BG })
  .jpeg({ quality: 92, mozjpeg: true })
  .toFile('public/favicon.jpg');

