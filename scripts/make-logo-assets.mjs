// Regenerate the shared web/PWA/logo source assets from the master brand image.
//
// Source of truth:
//   resources/brand/ultreia-original.png
//
// Outputs:
//   resources/ultreia-logo.png   — 384px transparent-corner app display logo
//   resources/ultreia-logo.webp  — same, lightweight runtime import
//   resources/ultreia-logo-final.png — 512px full original scaled down for the boot final frame
//   resources/splash-logo.png    — 512px transparent-corner native splash logo
//   public/favicon.jpg           — 512px full-square PWA / favicon / launcher base
//
// Android rasters and poster marks are generated by separate scripts because
// they have their own target dimensions and theme extraction rules.
import sharp from 'sharp';
import { mkdir } from 'node:fs/promises';

const SRC = 'resources/brand/ultreia-original.png';
const ROUNDED_RADIUS_RATIO = 0.22;
const FAVICON_BG = '#1a1a1a';
const CROP_LUMA_THRESHOLD = 10;

function roundedMaskSvg(size) {
  const radius = Math.round(size * ROUNDED_RADIUS_RATIO);
  return Buffer.from(
    `<svg width="${size}" height="${size}" xmlns="http://www.w3.org/2000/svg">
       <rect x="0" y="0" width="${size}" height="${size}" rx="${radius}" ry="${radius}" fill="#fff"/>
     </svg>`,
  );
}

async function findLogoCrop() {
  const { data, info } = await sharp(SRC)
    .removeAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });
  const { width, height, channels } = info;
  let minX = width;
  let minY = height;
  let maxX = -1;
  let maxY = -1;

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const i = (y * width + x) * channels;
      const r = data[i];
      const g = data[i + 1];
      const b = data[i + 2];
      const luma = 0.299 * r + 0.587 * g + 0.114 * b;
      if (luma <= CROP_LUMA_THRESHOLD) continue;
      if (x < minX) minX = x;
      if (y < minY) minY = y;
      if (x > maxX) maxX = x;
      if (y > maxY) maxY = y;
    }
  }

  if (maxX < minX || maxY < minY) {
    return { left: 0, top: 0, width, height };
  }

  const contentW = maxX - minX + 1;
  const contentH = maxY - minY + 1;
  const side = Math.min(Math.max(contentW, contentH), width, height);
  const cx = (minX + maxX) / 2;
  const cy = (minY + maxY) / 2;
  const left = Math.max(0, Math.min(width - side, Math.round(cx - side / 2)));
  const top = Math.max(0, Math.min(height - side, Math.round(cy - side / 2)));
  return { left, top, width: side, height: side };
}

const logoCrop = await findLogoCrop();

async function roundedLogo(size) {
  const base = await sharp(SRC)
    .extract(logoCrop)
    .resize(size, size, { fit: 'cover', position: 'centre' })
    .ensureAlpha()
    .png()
    .toBuffer();

  return sharp(base)
    .composite([{ input: roundedMaskSvg(size), blend: 'dest-in' }])
    .png()
    .toBuffer();
}

await mkdir('resources', { recursive: true });
await mkdir('public', { recursive: true });

const displayPng = await roundedLogo(384);
await sharp(displayPng).toFile('resources/ultreia-logo.png');
await sharp(displayPng)
  .webp({ quality: 90, alphaQuality: 100 })
  .toFile('resources/ultreia-logo.webp');

await sharp(SRC)
  .resize(512, 512, { fit: 'contain' })
  .png({ compressionLevel: 9, adaptiveFiltering: true })
  .toFile('resources/ultreia-logo-final.png');

const splashPng = await roundedLogo(512);
await sharp(splashPng).toFile('resources/splash-logo.png');

await sharp(splashPng)
  .flatten({ background: FAVICON_BG })
  .jpeg({ quality: 92, mozjpeg: true })
  .toFile('public/favicon.jpg');

console.log('✓ resources/ultreia-logo.png 384x384');
console.log('✓ resources/ultreia-logo.webp 384x384');
console.log('✓ resources/ultreia-logo-final.png 512x512');
console.log('✓ resources/splash-logo.png 512x512');
console.log('✓ public/favicon.jpg 512x512');
console.log(`  source crop: ${logoCrop.left},${logoCrop.top} ${logoCrop.width}x${logoCrop.height}`);
